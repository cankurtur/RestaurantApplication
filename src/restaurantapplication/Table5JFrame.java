    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed

        if (check == 1){
        Tables.table5Panel.setBackground(Color.red);
        Tables.available5Value.setText("Not Available");
        changeBill();
        }else{
            Tables.table5Panel.setBackground(Color.green);
            Tables.available5Value.setText("Available");
        }
       
        this.toBack();
        setVisible(false);
        // TODO add your handling code here:
    }//GEN-LAST:event_backButtonActionPerformed

    public void changeBill(){
        total = 0;
        for(int i = 0; i < table5Orders.getRowCount(); i++){
        
        int Amount = (int) table5Orders.getValueAt(i, 1);
        total = Amount+total;
            
    }
        Tables.bill5Value.setText(Integer.toString(total));
    }
