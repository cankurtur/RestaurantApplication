    private void checkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkButtonActionPerformed

        changeBill();
        totalAmount = 0;
        Tables.totalAmount += Integer.parseInt(Tables.bill5Value.getText());
        Tables.changeLabel();
        change();
        // TODO add your handling code here:
    }//GEN-LAST:event_checkButtonActionPerformed

    public void change (){
        check = 0;
        table5Orders.setModel(new DefaultTableModel(null, new String []{"Food Name","Food Price"}));
        Tables.table5Panel.setBackground(Color.green);
        Tables.available5Value.setText("Available");
        Tables.bill5Value.setText("0");
       
    }
    public static void AddRowToJTable(Object[] dataRow){
        model = (DefaultTableModel) table5Orders.getModel();
        model.addRow(dataRow);
        check = 1;
    }
